// Conditional statements
let number = 10;
if (number > 0) {
  console.log("Positive");
} else if (number < 0) {
  console.log("Negative");
} else {
  console.log("Zero");
}

// Loops
for (let i = 0; i < 5; i++) {
  console.log(i);
}

console.log("For loop ends ");

let j = 0;
while (j < 5) {
  console.log(j);
  j++;
}
